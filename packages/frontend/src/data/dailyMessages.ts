/**
 * Get today's message based on user's join date
 * Rotates through all messages in a consistent, predictable way
 */
export function getTodaysMessage(userJoinDate: Date): DailyMessage {
  const now = new Date();
  const joinDate = new Date(userJoinDate);

  // Calculate days since user joined
  const daysSinceJoin = Math.floor(
    (now.getTime() - joinDate.getTime()) / (1000 * 60 * 60 * 24)
  );

  // Get message index (rotates through all messages)
  const messageIndex = daysSinceJoin % dailyMessages.length;

  return dailyMessages[messageIndex];
}

/**
 * Get message by index
 */
export function getMessageByIndex(index: number): DailyMessage {
  const normalizedIndex = ((index % dailyMessages.length) + dailyMessages.length) % dailyMessages.length;
  return dailyMessages[normalizedIndex];
}

/**
 * Get current message index for a user
 */
export function getCurrentMessageIndex(userJoinDate: Date): number {
  const now = new Date();
  const joinDate = new Date(userJoinDate);

  const daysSinceJoin = Math.floor(
    (now.getTime() - joinDate.getTime()) / (1000 * 60 * 60 * 24)
  );

  return daysSinceJoin % dailyMessages.length;
}

/**
 * Get total number of messages
 */
export function getTotalMessages(): number {
  return dailyMessages.length;
}
